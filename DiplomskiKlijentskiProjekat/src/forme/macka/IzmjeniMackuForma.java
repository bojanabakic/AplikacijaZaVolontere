    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
        String kriterijum = txtImeMacke.getText().trim();

        if (kriterijum.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Za pretragu je potrebno unijeti ime mačke!");
            return;
        }

        for (int i = 0; i < kriterijum.length(); i++) {
            char provjera = kriterijum.charAt(i);
            if (isDigit(provjera)) {
                JOptionPane.showMessageDialog(this, "Ime mačke se sastoji samo od slova, ispraviti unijeto ime");
                return;
            }
        }

        Macka m = new Macka();
        m.setKriterijumPretrage(kriterijum);
        ArrayList<Macka> pretraga = KlijentKontroler.getInstance().pretraziMacku(m);
        srediFormuPoslijePretrage(pretraga);
    }//GEN-LAST:event_btnPretraziActionPerformed

