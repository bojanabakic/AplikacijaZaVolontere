    private void btnIzmjeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmjeniActionPerformed
        int red = tableMacke.getSelectedRow();

        if (red != -1) {
            ModelTabeleMacke mtm = (ModelTabeleMacke) tableMacke.getModel();
            Macka m = mtm.dajMacku(red);

            IzmjeniMackuDijalog imz = new IzmjeniMackuDijalog(this, true);
            imz.dodajMacku(m);
            imz.pack();
            imz.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Potrebno je selektovati mačku u tabeli koju želite da izmjenite");
            return;
        }
    }//GEN-LAST:event_btnIzmjeniActionPerformed

