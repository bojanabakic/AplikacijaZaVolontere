    public void srediModel() {
        ArrayList<Macka> macke = KlijentKontroler.getInstance().vratiSveMacke();
        ModelTabeleMacke mtm = new ModelTabeleMacke(macke);
        
        tableMacke.setFont(new Font("Arial", Font.PLAIN, 14));
        tableMacke.getTableHeader().setFont(new Font("Arial", Font.PLAIN, 14));
        tableMacke.setModel(mtm);
    }
    
    private void srediFormuPoslijePretrage(ArrayList<Macka> macke) {
        ModelTabeleMacke mtm = (ModelTabeleMacke) tableMacke.getModel();
        mtm.setLista(macke);
    }

