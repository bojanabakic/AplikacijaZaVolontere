    private void btnBrisanjeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBrisanjeActionPerformed
        int red = tableVolonter.getSelectedRow();

        if (red != -1) {
            ModelTabeleVolonter mtv = (ModelTabeleVolonter) tableVolonter.getModel();
            Volonter v = mtv.dajVolontera(red);
           
            BrisanjeVolonteraDijalog dijalog = new BrisanjeVolonteraDijalog(this, true);
            dijalog.dodajVolontera(v);
            dijalog.pack();
            dijalog.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Potrebno je selektovati mačku u tabeli koju želite da obrišete");
            return;
        }
    }//GEN-LAST:event_btnBrisanjeActionPerformed

