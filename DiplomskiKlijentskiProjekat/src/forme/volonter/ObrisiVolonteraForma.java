    public void srediModel() {
        ArrayList<Volonter> volonteri = KlijentKontroler.getInstance().vratiSveVolontere();

        ModelTabeleVolonter mtm = new ModelTabeleVolonter(volonteri);

        tableVolonter.setFont(new Font("Arial", Font.PLAIN, 14));
        tableVolonter.getTableHeader().setFont(new Font("Arial", Font.PLAIN, 14));
        tableVolonter.setModel(mtm);
    }

    private void srediFormuPoslijeBrisanja(ArrayList<Volonter> pretraga) {
        ModelTabeleVolonter mtv = (ModelTabeleVolonter) tableVolonter.getModel();
        mtv.setLista(pretraga);
    }

