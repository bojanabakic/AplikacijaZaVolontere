    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
        if ((!checkIme.isSelected() && !checkPrezime.isSelected()) || (checkIme.isSelected() && txtIme.getText().isEmpty())
                || (checkPrezime.isSelected() && txtPrezime.getText().isEmpty())) {
            JOptionPane.showMessageDialog(this, "Za pretragu je potrebno unijeti ime i/ili prezime volontera!");
            return;
        }

        if (checkPrezime.isSelected() && checkIme.isSelected()) {
            String prezime = txtPrezime.getText().trim();

            for (int i = 0; i < prezime.length(); i++) {
                char provjera = prezime.charAt(i);
                if (isDigit(provjera)) {
                    JOptionPane.showMessageDialog(this, "Prezime ne smije da sadrži brojeve");
                    return;
                }
            }

            String ime = txtIme.getText().trim();

            for (int i = 0; i < ime.length(); i++) {
                char provjera = ime.charAt(i);
                if (isDigit(provjera)) {
                    JOptionPane.showMessageDialog(this, "Ime ne smije da sadrži brojeve");
                    return;
                }
            }

            ArrayList<String> kriterijum = new ArrayList<>();
            kriterijum.add(ime);
            kriterijum.add(prezime);
            Volonter v = new Volonter();
            v.setKriterijumPretrage(kriterijum);

            ArrayList<Volonter> pretraga = KlijentKontroler.getInstance().pretraziVolontere(v);
            srediFormuPoslijePretrage(pretraga);
        }

        if (checkPrezime.isSelected() && !checkIme.isSelected()) {
            String prezime = txtPrezime.getText().trim();

            for (int i = 0; i < prezime.length(); i++) {
                char provjera = prezime.charAt(i);
                if (isDigit(provjera)) {
                    JOptionPane.showMessageDialog(this, "Prezime ne smije da sadrži brojeve");
                    return;
                }
            }

            ArrayList<String> kriterijum = new ArrayList<>();
            kriterijum.add(new String());
            kriterijum.add(prezime);
            Volonter v = new Volonter();
            v.setKriterijumPretrage(kriterijum);

            ArrayList<Volonter> pretraga = KlijentKontroler.getInstance().pretraziVolontere(v);
            srediFormuPoslijePretrage(pretraga);
        }

        if (checkIme.isSelected()) {
            String ime = txtIme.getText().trim();

            for (int i = 0; i < ime.length(); i++) {
                char provjera = ime.charAt(i);
                if (isDigit(provjera)) {
                    JOptionPane.showMessageDialog(this, "Ime ne smije da sadrži brojeve");
                    return;
                }
            }

            ArrayList<String> kriterijum = new ArrayList<>();
            kriterijum.add(ime);
            kriterijum.add(new String());
            Volonter v = new Volonter();
            v.setKriterijumPretrage(kriterijum);

            ArrayList<Volonter> pretraga = KlijentKontroler.getInstance().pretraziVolontere(v);
            srediFormuPoslijePretrage(pretraga);
        }
    }//GEN-LAST:event_btnPretraziActionPerformed

