    private void btnIzmjeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmjeniActionPerformed
        int red = tableVolonter.getSelectedRow();

        if (red != -1) {
            ModelTabeleVolonter mtv = (ModelTabeleVolonter) tableVolonter.getModel();
            Volonter v = mtv.dajVolontera(red);

            IzmjeniVolonteraDijalog ivz = new IzmjeniVolonteraDijalog(this, true);
            ivz.dodajVolontera(v);
            ivz.pack();
            ivz.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Potrebno je selektovati volontera u tabeli koju želite da izmjenite");
            return;
        }
    }//GEN-LAST:event_btnIzmjeniActionPerformed

