    private void btnDetaljnijeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetaljnijeActionPerformed
        int red = tableZaduzenje.getSelectedRow();

        if (red != -1) {
            ModelTabeleZaduzenje mtz = (ModelTabeleZaduzenje) tableZaduzenje.getModel();
            Zaduzenje z = mtz.dajZaduzenje(red);

            PregledZaduzenjaForma pzd = new PregledZaduzenjaForma();
            pzd.dodajZaduzenje(z);
            pzd.dodajGF(this);
            pzd.srediTabelu();
            pzd.dodajKorisnika(KlijentKontroler.getInstance().getTrenutnoUlogovanKorisnik());
            pzd.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Potrebno je selektovati zaduženje u tabeli.");
            return;
        }
    }//GEN-LAST:event_btnDetaljnijeActionPerformed

