    public void srediModel() {
        ArrayList<Zaduzenje> lista = KlijentKontroler.getInstance().vratiSvaZaduzenja();

        if (lista.isEmpty()) {
            mtz = new ModelTabeleZaduzenje();
        } else {
            mtz = new ModelTabeleZaduzenje(lista);
            mtz.sortiraj();
        }

        tableZaduzenje.setFont(new Font("Arial", Font.PLAIN, 14));
        tableZaduzenje.getTableHeader().setFont(new Font("Arial", Font.PLAIN, 14));
        tableZaduzenje.setModel(mtz);
    }

    private void centriraj() {
        this.setLocationRelativeTo(null);
    }

    private void izlogujKorisnika() {
        String porukaSaServera = KlijentKontroler.getInstance().zavrsiSaRadom();
        JOptionPane.showMessageDialog(this, porukaSaServera, "Uspješno izlogovani", JOptionPane.INFORMATION_MESSAGE);
    }
